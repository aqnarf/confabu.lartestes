function renderCategoryBento() {
  const target = document.querySelector("[data-category-bento]");
  if (!target) return;

  const categoryNames = categories().filter((category) => category !== "Todos");
  target.innerHTML = categoryNames.map((category, index) => {
    const count = books.filter((book) => book.category === category).length;
    const colors = bentoColors[index % bentoColors.length];
    return `
      <a class="bento-card" href="busca.html?category=${encodeURIComponent(category)}" style="--bento-a: ${colors[0]}; --bento-b: ${colors[1]}; --bento-ink: ${colors[2]}">
        <span class="bento-count">${count} obra${count === 1 ? "" : "s"}</span>
        <div>
          <h3>${category}</h3>
          <p>${categoryCopy[category] || "Uma seleção para descobrir novas leituras."}</p>
        </div>
      </a>
    `;
  }).join("");
}

function createChips(target, items, key) {
  if (!target) return;
  target.innerHTML = items.map((item) => `
    <button class="chip ${state[key] === item ? "active" : ""}" type="button" data-filter-key="${key}" data-filter-value="${item}">
      ${item}
    </button>
  `).join("");
}

function renderFilters() {
  createChips(document.querySelector("[data-category-filters]"), categories(), "category");
  createChips(document.querySelector("[data-age-filters]"), ages(), "age");
  createChips(document.querySelector("[data-format-filters]"), formats(), "format");
}

function filteredBooks() {
  const normalizedQuery = state.query.trim().toLowerCase();
  return books.filter((book) => {
    const matchesCategory = state.category === "Todos" || book.category === state.category;
    const matchesAge = state.age === "Todas" || book.age === state.age;
    const matchesFormat = state.format === "Todos" || book.format === state.format;
    const searchable = `${book.title} ${book.author} ${book.category} ${book.description}`.toLowerCase();
    const matchesQuery = !normalizedQuery || searchable.includes(normalizedQuery);
    return matchesCategory && matchesAge && matchesFormat && matchesQuery;
  });
}

function renderResults() {
  const results = document.querySelector("[data-book-results]");
  const count = document.querySelector("[data-results-count]");
  if (!results || !count) return;

  const visibleBooks = filteredBooks();
  count.textContent = `${visibleBooks.length} obra${visibleBooks.length === 1 ? "" : "s"} encontrada${visibleBooks.length === 1 ? "" : "s"}`;
  results.innerHTML = visibleBooks.length
    ? visibleBooks.map(bookCard).join("")
    : `<p class="empty-state">Nenhuma obra encontrada com esses filtros.</p>`;
}

function setupSearch() {
  if (!document.querySelector("[data-book-results]")) return;

  const params = new URLSearchParams(window.location.search);
  if (params.get("category") && categories().includes(params.get("category"))) {
    state.category = params.get("category");
  }
  if (params.get("age") && ages().includes(params.get("age"))) {
    state.age = params.get("age");
  }
  if (params.get("format") && formats().includes(params.get("format"))) {
    state.format = params.get("format");
  }
  if (params.get("q")) {
    state.query = params.get("q");
  }

  renderFilters();
  document.querySelector("[data-search-input]").value = state.query;
  renderResults();

  document.addEventListener("click", (event) => {
    const chip = event.target.closest("[data-filter-key]");
    if (!chip) return;
    state[chip.dataset.filterKey] = chip.dataset.filterValue;
    renderFilters();
    renderResults();
  });

  document.querySelector("[data-search-input]").addEventListener("input", (event) => {
    state.query = event.target.value;
    renderResults();
  });

  document.querySelector("[data-clear-filters]").addEventListener("click", () => {
    state.category = "Todos";
    state.age = "Todas";
    state.format = "Todos";
    state.query = "";
    document.querySelector("[data-search-input]").value = "";
    renderFilters();
    renderResults();
  });
}

function selectedBook() {
  const params = new URLSearchParams(window.location.search);
  return books.find((book) => book.id === params.get("id")) || books[0];
}

function recommendedBooks(currentBook) {
  const sameCategory = books.filter((book) => book.id !== currentBook.id && book.category === currentBook.category);
  const sameAge = books.filter((book) => book.id !== currentBook.id && book.age === currentBook.age && book.category !== currentBook.category);
  const remaining = books.filter((book) => book.id !== currentBook.id && !sameCategory.includes(book) && !sameAge.includes(book));
  return [...sameCategory, ...sameAge, ...remaining].slice(0, 4);
}

function setupBookPage() {
  if (!document.querySelector("[data-book-page]")) return;

  const book = selectedBook();
  document.title = `${book.title} | Estante Infantil`;
  document.querySelector("[data-book-category]").textContent = book.category;
  document.querySelector("[data-book-title]").textContent = book.title;
  document.querySelector("[data-book-author]").textContent = book.author;
  document.querySelector("[data-book-description]").textContent = book.description;
  document.querySelector("[data-book-quote]").textContent = book.excerpt[0];
  document.querySelector("[data-book-age]").textContent = book.age;
  document.querySelector("[data-book-format]").textContent = book.format;
  document.querySelector("[data-book-time]").textContent = book.time;
  document.querySelector("[data-book-cover]").innerHTML = coverTemplate(book);
  document.querySelector("[data-read-link]").href = `leitor.html?id=${book.id}`;
  document.querySelector("[data-recommended-books]").innerHTML = recommendedBooks(book).map(bookCard).join("");

  const modal = document.querySelector("[data-terms-modal]");
  const termsCheck = document.querySelector("[data-terms-check]");
  const downloadButton = document.querySelector("[data-download-book]");

  document.querySelector("[data-open-terms]").addEventListener("click", () => {
    termsCheck.checked = false;
    downloadButton.disabled = true;
    modal.showModal();
  });

  termsCheck.addEventListener("change", () => {
    downloadButton.disabled = !termsCheck.checked;
  });

  downloadButton.addEventListener("click", () => {
    if (book.pdfUrl) {
      const link = document.createElement("a");
      link.href = book.pdfUrl;
      link.download = `${book.id}.pdf`;
      link.click();
      modal.close();
      return;
    }

    const content = `${book.title}\n${book.author}\n\n${book.excerpt.join("\n\n")}\n\nUso educacional. Não redistribuir ou comercializar.`;
    const blob = new Blob([content], { type: "text/plain;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const link = document.createElement("a");
    link.href = url;
    link.download = `${book.id}.txt`;
    link.click();
    URL.revokeObjectURL(url);
    modal.close();
  });
}

function setupReaderPage() {
  if (!document.querySelector("[data-reader-page]")) return;

  const book = selectedBook();
  document.title = `Lendo ${book.title} | Estante Infantil`;
  document.querySelector("[data-reader-title]").textContent = book.title;
  document.querySelector("[data-reader-text]").innerHTML = book.excerpt.map((paragraph) => `<p>${paragraph}</p>`).join("");
  document.querySelector("[data-reader-format]").textContent = book.pdfUrl ? "PDF" : "Prévia textual";
  document.querySelector("[data-book-back]").href = `livro.html?id=${book.id}`;

  const reader = document.querySelector("[data-reader]");
  const pdfViewer = document.querySelector("[data-pdf-viewer]");
  if (book.pdfUrl) {
    reader.classList.add("has-pdf");
    pdfViewer.src = `${book.pdfUrl}#toolbar=0&navpanes=0`;
  } else {
    reader.classList.remove("has-pdf");
    pdfViewer.removeAttribute("src");
  }

  document.querySelector("[data-font-down]").addEventListener("click", () => {
    const currentSize = Number.parseFloat(getComputedStyle(reader).getPropertyValue("--reader-size"));
    reader.style.setProperty("--reader-size", `${Math.max(currentSize - 0.08, 0.9)}rem`);
  });

  document.querySelector("[data-font-up]").addEventListener("click", () => {
    const currentSize = Number.parseFloat(getComputedStyle(reader).getPropertyValue("--reader-size"));
    reader.style.setProperty("--reader-size", `${Math.min(currentSize + 0.08, 1.45)}rem`);
  });

  document.querySelectorAll("[data-theme]").forEach((button) => {
    button.addEventListener("click", () => {
      reader.classList.remove("theme-sepia", "theme-dark");
      if (button.dataset.theme !== "light") {
        reader.classList.add(`theme-${button.dataset.theme}`);
      }
      document.querySelectorAll("[data-theme]").forEach((item) => item.classList.remove("active"));
      button.classList.add("active");
    });
  });
}

function normalizeBook(book) {
  return {
    ...book,
    colors: Array.isArray(book.colors) && book.colors.length >= 2 ? book.colors : ["#d8f0e4", "#ff8e78"],
    ink: book.ink || "#101113",
    excerpt: Array.isArray(book.excerpt) && book.excerpt.length ? book.excerpt : [book.description || ""],
    featured: Boolean(book.featured),
    pdfUrl: book.pdfUrl || ""
  };
}

async function loadBooksFromApi() {
  if (!["http:", "https:"].includes(window.location.protocol)) return;

  try {
    const response = await fetch("/api/books");
    if (!response.ok) return;

    const apiBooks = await response.json();
    if (Array.isArray(apiBooks) && apiBooks.length) {
      books = apiBooks.map(normalizeBook);
    }
  } catch (error) {
    console.info("Usando livros locais: API indisponivel.", error);
  }
}

async function initApp() {
  await loadBooksFromApi();
  renderEditorialPick();
  renderFeatured();
  renderCategoryBento();
  setupSearch();
  setupBookPage();
  setupReaderPage();
}

initApp();
